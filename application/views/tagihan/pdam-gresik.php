                        <h2>Tentang PDAM Gresik</h2>
                        <p>
                            Perusahaan Daerah Air Minum kota Gresik atau yang dikenal sebagai Giri Tirta ialah Badan Usaha Pemerintah Daerah yang bergerak di bidang penyediaan layanan air bersih untuk dikonsumsi.
                        </p>
                        <p>
                            PDAM kota Gresik menyediakan air yang sesuai dengan standar kebersihan dan kesehatan Nasional. PDAM kota Gresik juga memastikan kebutuhan akan air minum masyarakat Gresik terpenuhi dengan baik.
                        </p>
                        <p>
                            Hal ini sesuai dengan yang tercantum dalam Undang-Undang Nomor 7 Tahun 2004 mengenai Sumber Daya Air, di mana pengembangan sistem penyediaan air minum menjadi tanggung jawab Pemerintah dan Pemerintah Daerah agar kesejahteraan masyarakat bisa terwujud.
                        </p>
                        <p>
                            Dilanjutkan dengan Undang-Undang 32 Tahun 2004 yang menjelaskan bahwa pelayanan air minum bagi masyarakat merupakan kewajiban Pemerintah Kabupaten/Kota, dimana kebijakan dan pelaksanaannya dilakukan oleh PDAM. Karenanya, dalam hal ini, pemerintah bertugas sebagai regulator, sedangkan PDAM berperan sebagai operator.
                        </p>

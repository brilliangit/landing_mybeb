                        <h2>Perusahaan Telepon Rumah di Indonesia</h2>
                        <p>
                            Berbeda dengan layanan jaringan internet, di Indonesia, layanan telepon rumah hanya disediakan oleh PT. Telekomunikasi Indonesia (TELKOM) sebagai salah satu Badan Usaha Milik Negara (BUMN). Hingga kini pelanggan telepon rumah sudah mencapai 15 juta pelanggan.
                        </p>
                        <p>
                            Telkom memulai menyediakan fasilitas telepon rumah pada tahun 1961 dengan status Perusahaan Negara Pos dan Telekomunikasi (PN Postel). Pada tahun 1965, PN Postel kemudian dibagi menjadi dua perusahaan yaitu Perusahaan Negara Pos dan Giro (PN Pos & Giro) dan Perusahaan Negara Telekomunikasi (PN Telekomunikasi).
                        </p>

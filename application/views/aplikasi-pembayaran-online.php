                        <h2>
                            1. MyBeb
                        </h2>
                        <p>
                            Aplikasi pembayaran terlengkap yang pertama adalah MyBeb. Aplikasi besutan PT Integra Data Solusi ini terbilang memiliki layanan yang sangat lengkap. MyBeb memanjakan penggunanya dengan 350 layanan lebih. Selain itu untuk menikmati itu semua Anda tidak harus mengeluarkan biaya pendaftaran sedikit pun, alias gratis.
                        </p>
                        <p>
                            Aplikasi media sosial berbasis payment ini juga menawarkan fitur media sosial. Anda bisa melakukan aktivitas seperti posting foto/video, membuat status, komentar, like, chatting dengan teman, membuat grup, hingga membaca berita online.
                        </p>
                        <p>
                            Yang lebih menggembirakannya lagi, setiap hari MyBeb membagikan voucher gratis kepada penggunanya. Voucher itu bisa didapatkan melalui penukaran poin CINTA yang didapat dari aktivitas di MyBeb.
                        </p>
                        <p>
                            Beberapa voucher gratis yang bisa Anda dapatkan antara lain, voucher KFC gratis, voucher McD gratis, pulsa gratis, uang tunai, hingga liburan gratis ke Singapura.
                        </p>
                        <h2>
                            2. OVO
                        </h2>
                        <p>
                            Aplikasi pembayaran pertama yang bisa Anda coba ialah OVO. Aplikasi ini sudah bekerja bersama dengan Grab hingga Anda bisa lakukan pembayaran non tunai saat memakai transportasi online ini. 
                        </p>
                        <p>
                            Selain itu, OVO sudah bekerja bersama dengan beberapa ratus merchant yang siap menganakemaskan Anda untuk memperoleh promosi serta cashback menarik. Tidak tanggung-tanggung chasback berbentuk OVO kontan itu dapat sampai 60%.
                        </p>
                        <p>
                            Beberapa merchant yang telah bekerja bersama dengan OVO salah satunya Hypermart, Matahari Departement Store, Ramayana, A&W, Cinemax, Tokopedia, Foodmart serta ada banyak yang lain.
                        </p>
                        <p>
                            Saya sendiri sekarang memakai aplikasi pembayaran OVO. Waktu saya berbelanja pakaian di Matahari Departement Store promosi chasback saat itu 50%. Casback berbentuk point yang saya bisa dari berbelanja Rp 500 beberapa ribu dapat sampai Rp 200 beberapa ribu lebih. Benar-benar menyenangkan bukan.
                        </p>
                        <h2>
                            3. Go-Pay
                        </h2>
                        <p>
                            Bila Grab punyai OVO, karena itu Go-Jek mempunyai Go-Pay. Dengan mempunyai saldo Go-Pay, Anda dapat lakukan pembayaran non tunai saat memakai layanan transportasi online Go-Jek baik itu Go-Ride, Go-Car dan pesan minuman dan makanan sebagai relasi Go-Pay. Umumnya biaya semakin lebih murah daripada membayar dengan tunai.
                        </p>
                        <p>
                            Beberapa produk serta feature Go-Pay salah satunya
                        </p>

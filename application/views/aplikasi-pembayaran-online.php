                            <li>Go Bills. Fitur ini mempermudah Anda untuk membayar tagihan seperti BPJS, PDAM, tagihan listrik serta token listrik.</li>
                            <li>Transfer Go-Pay. Anda dapat lakukan transaksi pada pemakai Go-Pay yang lain.</li>
                            <li>Tarik Tunai Go-Pay. Saldo Go-Pay dapat juga ditarik dengan tunai melalui rekening bank Anda. Tetapi untuk dapat mengerjakannya, account Go-Pay Anda harus upgrade dengan mengikutkan photo KTP serta photo diri.</li>
